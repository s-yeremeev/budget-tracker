function P({ children }: { children: ReactNode }) {
  return <p className="text-sm leading-relaxed text-fg-muted">{children}</p>;
}
function UL({ children }: { children: ReactNode }) {
  return <ul className="ml-1 mt-2 space-y-1.5 text-sm text-fg-muted">{children}</ul>;
}
function LI({ children }: { children: ReactNode }) {
  return (
    <li className="flex gap-2">
      <span className="mt-2 h-1.5 w-1.5 shrink-0 rounded-full bg-primary" />
      <span className="leading-relaxed">{children}</span>
    </li>
  );
}
function B({ children }: { children: ReactNode }) {
  return <span className="font-medium text-fg">{children}</span>;
}
function Kbd({ children }: { children: ReactNode }) {
  return (
    <kbd className="rounded-md border border-border bg-surface-2 px-1.5 py-0.5 text-xs font-medium text-fg">
      {children}
    </kbd>
  );
}

